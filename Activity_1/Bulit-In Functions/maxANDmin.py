#to find the maximum from a list/tuple/set
''' Max() : The max() function returns the largest item in an iterable. It can also be used to find the largest item between two or more parameter '''
tuple = (2,3,5)
print("Tuple =",tuple)
print("MAXIMUM =",max(tuple))

#to find the minimum from a list/tuple/set
''' min() : The min() function returns the smallest item in an iterable. It can also be used to find the smallest item between two or more parameter '''
tuple = (2,3,5)
print("Tuple =",tuple)
print("MINIMUM =",min(tuple))