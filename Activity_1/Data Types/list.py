'''List is an ordered sequence of items.All the items in a list do not need to be of the same type.A Liat is declared using [] brackets.'''
list = [30,'python',12,67,'hello']
print("LIST =",list)
print(type(list))
#prints the element present at index 2
print("List[2] = ", list[2])
#prints the element present at 0,1,2
print("List[0:3] = ", list[0:3])
#prints the elements starting from index 1
print("List[1:] = ", list[1:])
#prints the element at last index
print("List[-1] = ", list[-1])
#prints the element from starting index to 3
print("List[:4] = ", list[:4])