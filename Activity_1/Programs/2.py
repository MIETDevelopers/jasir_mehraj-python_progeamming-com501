#Python function to sum all the members in a list
# using sum function
list = [10,20,30,40,50]
print("List =",list)
print("The sum of members of list is = ",sum(list))

# using count variable
listA = [10,20,30,40,50]
print("List =",listA)
count = 0
for num in listA:
    count = count + num  
print("SUM = ",count)